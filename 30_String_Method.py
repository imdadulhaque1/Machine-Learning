#----------------String Methods Part-01---------------------
n =input("Please enter the text: ")
#   1.  len()          function
print("Length of string: ",len(n))
#   2.  lower()     method
print("Lower case of string: ",n.lower())
#   3.  upper()     method
print("Upper case of string: ",n.upper())
#   4.  title()        method
print("Title of string: ",n.title())
#   5.  count()     method
print("Count of string: ",n.count("a"))
